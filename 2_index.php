<p><h3>Ссылки и массивы</h3></p>
<p>Дан массив. Сделайте так, чтобы с помощью GET-запроса можно было вывести любой элемент этого массива. Для этого вручную сделайте ссылку для каждого элемента массива. Переходя любой ссылке мы должны увидеть на экране соответствующий элемент массива.</p>
<?php
$arr = ['США', 'Россия', 'Канада', 'Япония'];
if (isset($_GET['get']) and isset($arr[$_GET['get']])) echo $arr[$_GET['get']];
?>
<br>
<a href="?get=0">ссылка 0</a><br>
<a href="?get=1">ссылка 1</a><br>
<a href="?get=2">ссылка 2</a><br>
<a href="?get=3">ссылка 3</a><br>
<a href="?get=4">ссылка 4</a>

<p>Модифицируйте предыдущую задачу так, чтобы ссылки выводились с помощью цикла foreach автоматически в соответствии с количеством элементов в массиве.</p>
<?php
$arr = ['США', 'Россия', 'Канада', 'Япония'];
if (isset($_GET['get']) and isset($arr[$_GET['get']])) echo $arr[$_GET['get']];
echo '<br>';
foreach($arr as $key => $item) {
    echo "<p><a href=\"?get=$key\">ссылка $item</a></p>";
}
?>