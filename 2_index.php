<?php
if (isset($_GET['get'])) echo 'Число = '.$_GET['get'];
for ($i = 1; $i <=10; $i++) {
    echo "<p><a href=\"?get=$i\">ссылка $i</a></p>";
}
?>
<p>Модифицируйте предыдущую задачу так, чтобы каждая ссылка стояла в своем li в теге ul.</p>
