<b><h3>GET-запросы</h3></b>
<p>Отправьте с помощью GET-запроса число. Выведите его на экран.</p>
<?php
echo $_GET['get']; //?get=11
?>
<p>Отправьте с помощью GET-запроса число. Выведите его на экран квадрат этого числа.</p>
<?php
echo $_GET['get'] * $_GET['get']; //?get=11;
?>
<p>Отправьте с помощью GET-запроса два числа. Выведите его на экран сумму этих чисел.</p>
<?php
echo $_GET['get_1'] + $_GET['get_2']; //?get_1=22&get_2=33;
?>
<p>Пусть с помощью GET-запроса отправляется число. Оно может быть или 1, или 2. Сделайте так, чтобы если передано 1 - на экран вывелось слово 'привет', а если 2 - то слово 'пока'.</p>
<?php
if ($_GET['get'] == 1) echo 'Привет'; //?get=1
if ($_GET['get'] == 2) echo 'Пока'; //?get=2
?>
